# Python program using stack implementation

stack = []

# append function to push  element into the stack

stack.append('eat')
stack.append('code')
stack.append('repeat')
stack.append('sleep')

print('Initial stack')
print(stack)

# pop fucntion to pop element from stack

print('\nElements poped from stack:')
print(stack.pop())

print('\nStack after elements are poped:')
print(stack)

